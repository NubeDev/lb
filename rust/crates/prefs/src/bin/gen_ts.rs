//! `gen-prefs-ts` — emit the closed axis vocabulary (dimensions, units, and the simple style enums)
//! as a TypeScript constants module the client imports, so the prefs settings UI and the fieldConfig
//! unit picker **cannot disagree with the server** about the allowed set (prefs scope: "a generated
//! constants module … client and server can't disagree on the allowed set").
//!
//! Run from the workspace root: `cargo run -p lb-prefs --bin gen-prefs-ts > \
//!   ../ui/src/lib/prefs/dimensions.generated.ts`. A test (`tests/generated_ts_test.rs`) asserts the
//! checked-in file matches this output, so a drift fails CI rather than shipping a stale picker.

use lb_prefs::axis::{DateStyle, Dimension, FirstDay, NumberFormat, TimeStyle, Unit, UnitSystem};

fn main() {
    print!("{}", render());
}

/// The full generated module text (also used by the equality test).
pub fn render() -> String {
    let mut s = String::new();
    s.push_str("// GENERATED by `cargo run -p lb-prefs --bin gen-prefs-ts`. DO NOT EDIT.\n");
    s.push_str(
        "// The closed prefs vocabulary — the single source of truth shared with the server.\n\n",
    );

    // Dimensions.
    s.push_str("export const DIMENSIONS = [\n");
    for d in Dimension::ALL {
        s.push_str(&format!("  {:?},\n", d.as_str()));
    }
    s.push_str("] as const;\n");
    s.push_str("export type Dimension = (typeof DIMENSIONS)[number];\n\n");

    // Units grouped by their dimension, plus a flat list and a dimension lookup.
    s.push_str("export const UNITS = [\n");
    for u in Unit::ALL {
        s.push_str(&format!("  {:?},\n", u.as_str()));
    }
    s.push_str("] as const;\n");
    s.push_str("export type Unit = (typeof UNITS)[number];\n\n");

    s.push_str("export const UNIT_DIMENSION: Record<Unit, Dimension> = {\n");
    for u in Unit::ALL {
        s.push_str(&format!(
            "  {:?}: {:?},\n",
            u.as_str(),
            u.dimension().as_str()
        ));
    }
    s.push_str("};\n\n");

    // The simple style axes.
    s.push_str(&string_const(
        "DATE_STYLES",
        DateStyle::ALL.iter().map(|x| x.as_str()),
    ));
    s.push_str(&string_const(
        "TIME_STYLES",
        TimeStyle::ALL.iter().map(|x| x.as_str()),
    ));
    s.push_str(&string_const(
        "UNIT_SYSTEMS",
        UnitSystem::ALL.iter().map(|x| x.as_str()),
    ));
    s.push_str(&string_const(
        "NUMBER_FORMATS",
        NumberFormat::ALL.iter().map(|x| x.as_str()),
    ));
    s.push_str(&string_const(
        "FIRST_DAYS",
        FirstDay::ALL.iter().map(|x| x.as_str()),
    ));
    s.push_str(&string_const(
        "ENABLED_LANGUAGES",
        lb_prefs::ENABLED_LANGUAGES.iter().copied(),
    ));
    s
}

fn string_const<'a>(name: &str, values: impl Iterator<Item = &'a str>) -> String {
    let items: Vec<String> = values.map(|v| format!("{v:?}")).collect();
    format!("export const {name} = [{}] as const;\n", items.join(", "))
}
