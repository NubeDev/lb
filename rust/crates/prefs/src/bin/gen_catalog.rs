//! `gen-prefs-catalog` — emit the built-in en/es MF1 catalogs as a TypeScript module the client
//! bundle imports (i18n-catalogs scope: "a `gen_catalog` bin (twin of the shipped `gen_ts`) generates
//! `ui/src/lib/prefs/catalog.generated.ts`"). The client parses the SAME MF1 source strings with
//! `intl-messageformat`, so host and client render the same messages by construction.
//!
//! Run from the workspace root, redirecting into the consuming client's tree, e.g.
//!   `cargo run -p lb-prefs --bin gen-prefs-catalog > <client>/src/lib/prefs/catalog.generated.ts`
//!
//! lb is a library and the client lives out of tree, so nothing here is checked in for this to be
//! drift-tested against (the in-tree `ui/` went away in `678503f`). A consumer that vendors the
//! output owns that drift test. What this repo still guards is the source the output is derived
//! from: `tests/generated_catalog_test.rs` asserts the builtins parse and stay key-aligned.

use lb_prefs::catalog::{parse_builtin, EN_MF, ES_MF};

fn main() {
    print!("{}", render());
}

/// The full generated module text (also used by the drift test). Emits, per enabled locale, its
/// `catalog-version` and its flat key→MF1 map — the exact source strings the host renders from.
pub fn render() -> String {
    let mut s = String::new();
    s.push_str("// GENERATED by `cargo run -p lb-prefs --bin gen-prefs-catalog`. DO NOT EDIT.\n");
    s.push_str(
        "// The built-in MF1 message catalogs — the single source shared with the Rust host.\n",
    );
    s.push_str(
        "// The client parses these with intl-messageformat; the host with its MF1 subset\n",
    );
    s.push_str("// parser. Byte-identity with the host `.mf` assets is drift-tested.\n\n");

    s.push_str("export type BuiltinCatalog = {\n");
    s.push_str("  version: string;\n");
    s.push_str("  messages: Record<string, string>;\n");
    s.push_str("};\n\n");

    s.push_str("export const BUILTIN_CATALOGS: Record<string, BuiltinCatalog> = {\n");
    for (lang, src) in [("en", EN_MF), ("es", ES_MF)] {
        let cat = parse_builtin(src);
        s.push_str(&format!("  {lang:?}: {{\n"));
        s.push_str(&format!("    version: {:?},\n", cat.version));
        s.push_str("    messages: {\n");
        for (key, msg) in &cat.messages {
            s.push_str(&format!("      {key:?}: {msg:?},\n"));
        }
        s.push_str("    },\n");
        s.push_str("  },\n");
    }
    s.push_str("};\n");
    s
}
